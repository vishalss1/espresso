//! Display driver — SH1106 128×64 I2C OLED (1.3" module)
//!
//! Tile rendering only, no framebuffer.
//! 21 cols × 8 rows (6×8 font), 40-row scroll history.
//!
//! SH1106 differs from SSD1306:
//!   - Only page addressing mode (no horizontal)
//!   - 132-column internal RAM, visible area starts at column +2
//!   - Must set column/page address before each page write

pub const SCREEN_WIDTH: u8 = 128;
pub const SCREEN_HEIGHT: u8 = 64;
pub const CHAR_WIDTH: u8 = 6;
pub const CHAR_HEIGHT: u8 = 8;

pub const GRID_COLS: usize = 21;
pub const GRID_ROWS: usize = 8;
pub const SCROLL_ROWS: usize = 40;
pub const I2C_ADDR: u8 = 0x3C;

// ── Cell type ────────────────────────────────────────────────────────────────

#[derive(Copy, Clone)]
pub struct Cell {
    pub ch: u8,
    pub inv: bool,
}

impl Cell {
    pub const fn space() -> Self {
        Cell { ch: b' ', inv: false }
    }
}

// ── CharGrid ─────────────────────────────────────────────────────────────────

pub struct CharGrid {
    pub visible: [[Cell; GRID_COLS]; GRID_ROWS],
    pub scroll_buf: [[Cell; GRID_COLS]; SCROLL_ROWS],
    pub scroll_write: usize,
    pub cursor_col: usize,
    pub cursor_row: usize,
}

pub static mut GRID: CharGrid = CharGrid {
    visible: [[Cell { ch: b' ', inv: false }; GRID_COLS]; GRID_ROWS],
    scroll_buf: [[Cell { ch: b' ', inv: false }; GRID_COLS]; SCROLL_ROWS],
    scroll_write: 0,
    cursor_col: 0,
    cursor_row: 0,
};

static mut DIRTY_PAGES: u8 = 0; // bitmask: bit N = page N needs re-render

impl CharGrid {
    pub const fn new() -> Self {
        Self {
            visible: [[Cell { ch: b' ', inv: false }; GRID_COLS]; GRID_ROWS],
            scroll_buf: [[Cell { ch: b' ', inv: false }; GRID_COLS]; SCROLL_ROWS],
            scroll_write: 0,
            cursor_col: 0,
            cursor_row: 0,
        }
    }

    pub fn clear(&mut self) {
        self.visible = [[Cell::space(); GRID_COLS]; GRID_ROWS];
        self.cursor_col = 0;
        self.cursor_row = 0;
    }

    /// Push top row into scroll buffer, shift grid up, clear bottom row.
    fn scroll_up(&mut self) {
        if self.scroll_write >= SCROLL_ROWS {
            self.scroll_write = 0;
        }
        let row = self.visible[0];
        self.scroll_buf[self.scroll_write] = row;
        self.scroll_write = (self.scroll_write + 1) % SCROLL_ROWS;
        for r in 0..GRID_ROWS - 1 {
            self.visible[r] = self.visible[r + 1];
        }
        self.visible[GRID_ROWS - 1] = [Cell::space(); GRID_COLS];
        unsafe { DIRTY_PAGES = 0xFF; } // all pages shifted
    }

    /// Write a single character at the current cursor position, advancing cursor.
    pub fn put_char(&mut self, ch: u8) {
        if self.cursor_row >= GRID_ROWS { self.cursor_row = 0; }
        if self.cursor_col >= GRID_COLS { self.cursor_col = 0; }
        match ch {
            b'\n' => {
                unsafe { DIRTY_PAGES |= 1 << self.cursor_row; }
                self.cursor_col = 0;
                self.cursor_row += 1;
                if self.cursor_row >= GRID_ROWS {
                    self.scroll_up();
                    self.cursor_row = GRID_ROWS - 1;
                }
            }
            b'\r' => {
                self.cursor_col = 0;
            }
            b'\x08' => {
                // Backspace
                if self.cursor_col > 0 {
                    self.cursor_col -= 1;
                    self.visible[self.cursor_row][self.cursor_col] = Cell::space();
                    unsafe { DIRTY_PAGES |= 1 << self.cursor_row; }
                }
            }
            _ => {
                if self.cursor_col < GRID_COLS {
                    self.visible[self.cursor_row][self.cursor_col] =
                        Cell { ch, inv: false };
                    self.cursor_col += 1;
                    unsafe { DIRTY_PAGES |= 1 << self.cursor_row; }
                }
                if self.cursor_col >= GRID_COLS {
                    self.cursor_col = 0;
                    self.cursor_row += 1;
                    if self.cursor_row >= GRID_ROWS {
                        self.scroll_up();
                        self.cursor_row = GRID_ROWS - 1;
                    }
                }
            }
        }
    }

    pub fn write_str(&mut self, s: &str) {
        for &b in s.as_bytes() {
            self.put_char(b);
        }
    }
}

