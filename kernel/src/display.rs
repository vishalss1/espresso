/// Return a pointer to glyph bitmap for a given ASCII code.
#[inline]
pub fn glyph_ptr(ch: u8) -> *const u8 {
    unsafe {
        FONT_6X8.as_ptr().add((ch as usize) * FONT_HEIGHT)
    }
}

// ── SH1106 I2C commands ──────────────────────────────────────────────────────

fn send_cmd_batch(cmds: &[u8]) {
    let mut buf = [0u8; 16];
    for chunk in cmds.chunks(8) {
        buf[0] = 0x00; // control byte: command stream
        let mut len = 1;
        for &c in chunk {
            buf[len] = c;
            len += 1;
        }
        crate::drivers::i2c::write(I2C_ADDR, &buf[..len]);
    }
}

/// Initialize SH1106 for 128×64, page addressing, no scroll.
pub fn init() {
    // Wait >300ms after power-on
    crate::drivers::delay::ms(400);

    // Phase 1: basic config + charge pump enable
    send_cmd_batch(&[
        0xAE,       // Display OFF
        0xD5, 0x80, // Set display clock divide ratio
        0xA8, 0x3F, // Set multiplex ratio (64-1)
        0xD3, 0x00, // Display offset = 0
        0x40,       // Start line = 0
        0x8D, 0x14, // Charge pump: enable
    ]);

    // Wait for charge pump to stabilize (~100ms per datasheet)
    crate::drivers::delay::ms(100);

    // Phase 2: display config + ON
    // SH1106 uses page addressing mode (0x02), not horizontal
    send_cmd_batch(&[
        0x20, 0x02, // Memory addressing mode: PAGE (SH1106 only)
        0xA1,       // Segment re-map: col127 → SEG0
        0xC8,       // COM scan direction: remapped
        0xDA, 0x12, // COM pins config (128×64)
        0x81, 0xCF, // Contrast
        0xD9, 0xF1, // Pre-charge period
        0xDB, 0x40, // VCOMH deselect level
        0xA4,       // Entire display ON (follow RAM)
        0xA6,       // Normal display (not inverted)
        0x2E,       // Deactivate scroll
        0xAF,       // Display ON
    ]);
}

/// Set page and column start address for page addressing mode.
/// SH1106 RAM is 132 columns; visible area starts at column 2.
fn set_page_col(page: u8, col: u8) {
    let c = col + 2; // SH1106 column offset
    crate::drivers::i2c::write(I2C_ADDR, &[
        0x00,                // control byte: command
        0xB0 | (page & 0x07), // set page address
        0x00 | (c & 0x0F),   // lower nibble of column
        0x10 | ((c >> 4) & 0x0F), // upper nibble of column
    ]);
}

/// Send a page buffer (128 bytes = 1 page = 8 pixel rows) to the display.
pub fn send_page(page: u8, data: &[u8; 128]) {
    set_page_col(page, 0);
    // Send data: control byte 0x40 = data mode, then data in 32-byte chunks
    let mut buf = [0u8; 33];
    buf[0] = 0x40;
    let mut offset = 0usize;
    while offset < 128 {
        let chunk = core::cmp::min(32, 128 - offset);
        buf[1..=chunk].copy_from_slice(&data[offset..offset + chunk]);
        crate::drivers::i2c::write(I2C_ADDR, &buf[..=chunk]);
        offset += chunk;
    }
}

// ── Tile Renderer ────────────────────────────────────────────────────────────

/// SH1106 page buffer: 128 bytes (1 page = 8 pixel rows)
pub static mut PAGE_BUF: [u8; 128] = [0u8; 128];

/// Render one page (row) of the visible grid to the display.
fn render_page(grid: &CharGrid, page: usize) {
    unsafe {
        let mut col_bytes = [0u8; 128];

        for ch_idx in 0..GRID_COLS {
            let cell = grid.visible[page][ch_idx];
            let glyph = glyph_ptr(cell.ch);
            let px_start = ch_idx * 6;

            for col in 0..6usize {
                let px = px_start + col;
                if px < 128 {
                    let byte = core::ptr::read_volatile(glyph.add(col));
                    col_bytes[px] = if cell.inv { !byte } else { byte };
                }
            }
        }

        for px in (GRID_COLS * 6)..128 {
            col_bytes[px] = 0;
        }

        send_page(page as u8, &col_bytes);
    }
}

/// Render the visible grid to the display.
/// Font is column-based: each glyph byte = one vertical column, bit 0 = top pixel.
/// This maps directly to SH1106 page format — copy glyph bytes straight to column buffer.
pub fn render(grid: &CharGrid) {
    for page in 0..GRID_ROWS {
        render_page(grid, page);
    }
}

/// Clear the entire display (all pages to zero).
pub fn clear_display() {
    for page in 0..8u8 {
        set_page_col(page, 0);
        let mut buf = [0u8; 33];
        buf[0] = 0x40; // data mode
        // buf[1..] is already zeroed — send 128 zero bytes
        let mut offset = 0usize;
        while offset < 128 {
            let chunk = core::cmp::min(32, 128 - offset);
            crate::drivers::i2c::write(I2C_ADDR, &buf[..=chunk]);
            offset += chunk;
        }
    }
}

/// Full-screen render: render visible grid.
pub fn refresh_full(grid: &CharGrid) {
    render(grid);
}

/// Flush pending display updates. Only renders pages that changed since last flush.
/// Call from the shell loop to batch renders.
pub fn flush_display() {
    unsafe {
        let dirty = DIRTY_PAGES;
        if dirty != 0 {
            DIRTY_PAGES = 0;
            let grid = &GRID;
            for page in 0..GRID_ROWS {
                if dirty & (1 << page) != 0 {
                    render_page(grid, page);
                }
            }
        }
    }
}
